class Acid implements Tangible {

    public Acid(){
    }
    
    private int damagePoint;

    public Acid(int damagePoint) {
        this.damagePoint=damagePoint;
    }

    public void action(Player player) {
        player.takeDamage(this.damagePoint);
    }

    public char showAs() {
        return 'A';
    }
}
